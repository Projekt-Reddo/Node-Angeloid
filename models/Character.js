class Character {
    constructor(characterName, characterRole, characterImage, seiyuuName, seiyuuImage) {
        this.characterName = characterName;
        this.characterRole = characterRole;
        this.characterImage = characterImage;
        this.seiyuuName = seiyuuName;
        this.seiyuuImage = seiyuuImage;
    }
}

module.exports = Character;